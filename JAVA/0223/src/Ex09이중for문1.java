
public class Ex09이중for문1 {

	public static void main(String[] args) {
		
	// for문 예제 - 구구단 2단~9단까지 출력하세요.	
		
	for(int j = 2; j<10; j++) {
		System.out.println("== " + j + "단 ==");
		for(int i = 1; i<10; i++) {
			System.out.println(j + "*" + i + "=" + j*i);
		}
		
	}	
		
		
		

	}

}
