package Model;

public class HamburgerVO {

	int correctNum;
	String correctRecipe;
	int correctPrice;
	String correctBurger;
	
	
	
	
	public int getCorrectNum() {
		return correctNum;
	}
	public void setCorrectNum(int correctNum) {
		this.correctNum = correctNum;
	}
	
	
	public String getCorrectRecipe() {
		return correctRecipe;
	}
	public void setCorrectRecipe(String correctRecipe) {
		this.correctRecipe = correctRecipe;
	}
	
	
	public int getCorrectPrice() {
		return correctPrice;
	}
	public void setCorrectPrice(int correctPrice) {
		this.correctPrice = correctPrice;
	}
	
	
	public String getCorrectBurger() {
		return correctBurger;
	}
	public void setCorrectBurger(String correctBurger) {
		this.correctBurger = correctBurger;
	}
	
	
	
	
	
}
